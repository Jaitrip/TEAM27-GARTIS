    private void printButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printButtonActionPerformed
        // TODO add your handling code here:
        try {
            
            Document document = new Document();
            PdfWriter writer = PdfWriter.getInstance(document, new FileOutputStream("VehicleReport.pdf"));
            document.open();
            
            document.add(new Paragraph("Vehicle Report"));
            document.add(new Paragraph("Report start date: " + startDateField.getText()));
            document.add(new Paragraph("Report end date: " + endDateField.getText()));
            document.add(new Paragraph("Job Type: " + jobTypeBox.getSelectedItem().toString()));

            document.add(new Paragraph(vehiclesBookedLabel.getText()));
            
            document.close();
            writer.close();
            
            
        } catch (Exception e) {
            JFrame frame = new InvalidError();
            frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            frame.pack();
            frame.setVisible(true);
        }
    }//GEN-LAST:event_printButtonActionPerformed

    private void generateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateButtonActionPerformed
        // TODO add your handling code here:
        try {

            Connection connection = DBConnection.getConnection();
            String sqlQuery = " SELECT COUNT(*) FROM JobSheet WHERE dateBookedIn BETWEEN ? AND ?";
            String withJobsQuery = "SELECT COUNT(*) FROM JobSheet WHERE dateBookedIn BETWEEN ? AND ? AND jobtype = ?";
            String jobType = jobTypeBox.getSelectedItem().toString();
            
            if (!jobType.equals("All")) {
                PreparedStatement pStatement = connection.prepareStatement(withJobsQuery);
                pStatement.setString(1, startDateField.getText());
                pStatement.setString(2, endDateField.getText());
                pStatement.setString(3, jobType);
                ResultSet resultSet = pStatement.executeQuery();
                DefaultTableModel model = (DefaultTableModel) reportTable.getModel();
                model.setRowCount(0);
                while (resultSet.next()) {
                    Object o[] = {
                        resultSet.getString("COUNT(*)")
                    };
                    model.addRow(o);
                    
                    vehiclesBookedLabel.setText("Number of vehicles booked: " + resultSet.getString("COUNT(*)") );

                }

                pStatement.close();
                resultSet.close();
                connection.close();
            
            } else {
                
                PreparedStatement pStatement = connection.prepareStatement(sqlQuery);
                pStatement.setString(1, startDateField.getText());
                pStatement.setString(2, endDateField.getText());
                ResultSet resultSet = pStatement.executeQuery();
                DefaultTableModel model = (DefaultTableModel) reportTable.getModel();
                model.setRowCount(0);
                while (resultSet.next()) {
                    Object o[] = {
                        resultSet.getString("COUNT(*)")
                    };
                    model.addRow(o);
                    
                    vehiclesBookedLabel.setText("Number of vehicles booked: " + resultSet.getString("COUNT(*)") );

                }

                pStatement.close();
                resultSet.close();
                connection.close();

                
            }
            

            
        } catch (Exception e) {
            JFrame frame = new InvalidError();
            frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            frame.pack();
            frame.setVisible(true);
        }
    }//GEN-LAST:event_generateButtonActionPerformed

